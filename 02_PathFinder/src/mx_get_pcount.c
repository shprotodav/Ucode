int *mx_get_pcount(char **points_arr) {
    int *pcount = malloc(sizeof(int) * 1);

mx_printstr("10.2\n");///////////////
    *pcount = 0;
    mx_printstr("10.3\n");///////////////
    for (int i = 0; points_arr[i]; i++) {
        mx_printstr("10.n\n");///////////////
        (*pcount)++;
        mx_printstr("10.n1\n");///////////////
    }
        
    mx_printstr("10.4\n");///////////////
    return pcount;
}




